import type { SphereId } from "./types";
import { sphereById } from "./methodology";

// Простой словарь ключевых слов → сфера. Помогает мягко пометить запись
// дневника тегами, чтобы потом видеть, о чём чаще всего речь.
// Это НЕ оценка и не трекер — просто ориентир для самой участницы.
